package programming

/*
编写一个函数，其作用是将输入的字符串反转过来。输入字符串已字符数组 char[] 的形式给出
不要给另外的数组分配额外的空间，必须原地修改输入数组、使用O(1)的额外空间解决这一问题。
可以假设数组中的所有字符都是ASCII码表中的可打印字符。

示例1：
输入：['h','e','l','l','o']
输出：['o','l','l','e','h']

示例2：
输入：['H','a','n','n','a','h']
输出：['h','a','n','n','a','H']
*/

// 时间复杂度O(n)，空间复杂度O(1)
func reverseString(val []byte) {
	n := len(val)
	for i := 0; i < n/2; i++ {
		val[i], val[n-1-i] = val[n-1-i], val[i]
	}
}
